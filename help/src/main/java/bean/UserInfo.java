public class UserInfo {
    private int uid;
    private String uname;
    private String mobile;
    private String uintro;
    private String uavatarurl;
    private String uavatarbase64;
    private int age;
    private String school;
    private int sid;
    private String gender;
    private String major;
    private String token;

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getUintro() {
        return uintro;
    }

    public void setUintro(String uintro) {
        this.uintro = uintro;
    }

    public String getUavatarurl() {
        return uavatarurl;
    }

    public void setUavatarurl(String uavatarurl) {
        this.uavatarurl = uavatarurl;
    }

    public String getUavatarbase64() {
        return uavatarbase64;
    }

    public void setUavatarbase64(String uavatarbase64) {
        this.uavatarbase64 = uavatarbase64;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getSchool() {
        return school;
    }

    public void setSchool(String school) {
        this.school = school;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
